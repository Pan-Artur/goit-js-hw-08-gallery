const listEl = document.querySelector(".js-gallery");
const backdrop = document.querySelector(".lightbox");
const modalImageEl = document.querySelector(".lightbox__image");
const exit = document.querySelector(".lightbox__button");

images.map((image) => {
  const itemEl = document.createElement("li");
  itemEl.classList.add("gallery__item");

  const imageEl = document.createElement("img");
  imageEl.src = image.original;
  imageEl.dataset.link = image.preview;
  imageEl.classList.add("gallery__image");

  listEl.append(itemEl);

  itemEl.append(imageEl);
});

listEl.addEventListener("click", openModal);

function openModal(e) {
  if (e.target.closest("li")) {
    modalImageEl.src = e.target.closest(".gallery__image").dataset.link;

    backdrop.classList.add("is-open");

    listEl.removeEventListener("click", openModal);

    exit.addEventListener("click", closeModal);
    document.addEventListener("keydown", (e) => {
      if (e.code === "Escape") {
        closeModal();
      }
    });
  }
}

function closeModal(e) {
  backdrop.classList.remove("is-open");

  exit.removeEventListener("click", closeModal);
  document.removeEventListener("keydown", (e) => {
    if (e.code === "Escape") {
      closeModal();
    }
  });

  listEl.addEventListener("click", openModal);
}